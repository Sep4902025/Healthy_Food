// ðŸ“Œ Láº¥y thÃ´ng tin chi tiáº¿t má»™t ngÆ°á»i dÃ¹ng theo ID (bá» qua user Ä‘Ã£ xÃ³a)
exports.getUserById = catchAsync(async (req, res, next) => {
  const user = await UserModel.findOne({
    _id: req.params.id,
    isDelete: false, // Chá»‰ láº¥y user chÆ°a bá»‹ xÃ³a
  }).populate("userPreferenceId");

  if (!user) {
    return next(new AppError("User not found or has been deleted", 404));
  }

  res.status(200).json({
    status: "success",
    data: { user },
  });
});

// Update User By ID
exports.updateUserById = catchAsync(async (req, res, next) => {
  const { id } = req.params;
  const updates = req.body;

  const user = await UserModel.findByIdAndUpdate(id, updates, {
    new: true, // Tráº£ vá» user sau khi cáº­p nháº­t
    runValidators: true, // Cháº¡y validation trÃªn dá»¯ liá»‡u cáº­p nháº­t
  });

  if (!user || user.isDelete) return next(new AppError("User not found", 404));

  res.status(200).json({
    status: "success",
    message: "User updated successfully",
    data: { user },
  });
});

exports.deleteUser = catchAsync(async (req, res, next) => {
  const { id } = req.params; // Láº¥y userId tá»« URL

  // TÃ¬m user theo ID
  const user = await UserModel.findOne({ _id: id });

  if (!user || user.isDelete) {
    return next(new AppError("User not found or has been deleted", 404));
  }

  // Náº¿u user cÃ³ userPreferenceId, xÃ³a má»m luÃ´n dá»¯ liá»‡u preference
  if (user.userPreferenceId) {
    await UserPreferenceModel.findByIdAndUpdate(user.userPreferenceId, {
      isDelete: true,
    });
  }

  // XÃ³a má»m user báº±ng cÃ¡ch Ä‘áº·t isDelete = true
  user.isDelete = true;
  await user.save();

  res.status(200).json({
    status: "success",
    message: "User deleted successfully",
  });
});

// ðŸŸ¢ Restore user (Chá»‰ admin)
exports.restoreUser = catchAsync(async (req, res, next) => {
  const user = await UserModel.findByIdAndUpdate(
    req.params.id,

    { isDelete: false },

    { new: true }
  );

  if (!user) return next(new AppError("User not found", 404));

  res.status(200).json({
    status: "success",
    message: "User restored successfully",
    data: { user },
  });
});
